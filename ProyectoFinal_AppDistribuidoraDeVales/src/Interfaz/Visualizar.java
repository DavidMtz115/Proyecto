    private void btnAbrirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAbrirMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser("C:\\Archivos\\Clientes");// --> C:\\Archivos\\
        int resu = fileChooser.showOpenDialog(this);// this referencia a la clase BLOCKNOTAS
        if (resu == JFileChooser.APPROVE_OPTION) {
            //Abrió el archivo
            //ABRIR EL ARCHIVO
            InputStream in;
            InputStreamReader isReader = null;
            BufferedReader bufReader = null;

            try {
                Path path = fileChooser.getSelectedFile().toPath();
                in = Files.newInputStream(path);
                isReader = new InputStreamReader(in);
                bufReader = new BufferedReader(isReader); //inpoutStringReader
                txtABlockNotas.read(bufReader, "Abrir archivo...");
            } catch (IOException ex) {
                Logger.getLogger(Visualizar.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    bufReader.close();
                    isReader.close();
                } catch (IOException ex) {
                    Logger.getLogger(Visualizar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnAbrirMouseClicked

